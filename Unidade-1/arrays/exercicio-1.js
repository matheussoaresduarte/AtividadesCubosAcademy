
// crie um array com 5 nomes de paises
const nomeDepaises = ['brasil', 'argentina', 'paraguai', 'colombia', 'canada'];
// adicione um pais ao fim da lista
nomeDepaises.push('italia');
// imprima a lista na tela
console.log(nomeDepaises);
// remova um pais do fim da lista
nomeDepaises.pop();
// imprima a lista na tela
console.log(nomeDepaises);
// adicione um pais ao inicio da lista
nomeDepaises.unshift('russia');
// imprima a lista na tela
console.log(nomeDepaises);
// remova o pais do inicio da lista
nomeDepaises.shift();
// imprima a lista na tela
console.log(nomeDepaises);
// imprima o utimo pais da lista na tela
console.log(nomeDepaises[nomeDepaises.length - 1]);
// imprima o segundo pais da lista na tela
console.log(nomeDepaises[1]);
